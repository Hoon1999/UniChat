create table member
(
    id bigint generated by default as identity,
    m_id varchar(30),
    pw varchar(30),
    email varchar(40),
    qna varchar(20),
    name varchar(255),
    primary key(id),
    unique (m_id),
    unique (email)
);

drop table if exists participatingChatRoom;
create table participatingChatRoom
(
    id bigint,
    chat_id bigint,
    chat_seq int,
    last_date timestamp,
    primary key(id, chat_id),
    foreign key(id) reference member(id),
);
drop table if exists chatRoom;
create table chatRoom
(
    chat_id bigint generated by default as identity,
    name varchar(15),
    primary key(chat_id),
    foreign key(chat_id) reference participatingChatRoom(chat_id)
);
drop table if exists message;
create table message
(
    msg_id bigint generated by default as identity,
    sender_id bigint,
    receiver_id bigint,
    send_time timestamp,
    content varchar(255),
    primary key(msg_id)
);